# Types of variables in Python

# 1-integer
i = 4
print(type(i))


# 2-float
f = 7.5
print(type(f))


# 3-String
s = "Alireza Heyrani"
print(type(s))


# 4-bool
a = True
b = False
print(type(a))


# 5-complex
c = 12j 
print(type(c))

# 6-casting
print(hex(18562458715))
print(bin(18562458715))

phyton = 20
print(int(phyton))
print(float(phyton))
print(str(phyton))
print(bool(phyton))
print(type(phyton))